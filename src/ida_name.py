# Constantes SN_ (Set Name flags)
SN_CHECK = 0x00  # Fail if the name contains invalid characters.
SN_NOCHECK = 0x01  # Don't fail, replace invalid chars with '_'
SN_PUBLIC = 0x02  # Make name public
SN_NON_PUBLIC = 0x04  # Make name non-public
SN_WEAK = 0x08  # Make name weak
SN_NON_WEAK = 0x10  # Make name non-weak
SN_AUTO = 0x20  # Make name autogenerated
SN_NON_AUTO = 0x40  # Make name non-autogenerated
SN_NOLIST = 0x80  # Exclude from name list
SN_NOWARN = 0x100  # don't display a warning if failed
SN_LOCAL = 0x200  # Create local name
SN_IDBENC = 0x400  # the name is given in the IDB encoding; non-ASCII bytes will be decoded accordingly
SN_FORCE = 0x800  # if the specified name is already present in the database, try variations with a numerical suffix like "_123"
SN_NODUMMY = 0x1000  # automatically prepend the name with '_' if it begins with a dummy suffix such as 'sub_'.
SN_DELTAIL = (
    0x2000  # if name cannot be set because of a tail byte, delete the hindering item
)


def get_name(ea, flags):
    fcp = FlatProgramAPI(cp.currentProgram)
    listing = cp.currentProgram.getListing()
    minAddress = cp.currentProgram.minAddress.getOffset()
    codeUnit = listing.getCodeUnitAt(fcp.toAddr(minAddress + ea))
    ret = str()
    if codeUnit != None:
        name = codeUnit.getSymbols()
        if len(name):
            ret = str(name[0])
    return ret


def set_name(ea, name, flags=0):
    """
    Changes the primary symbol name at the given address `ea`, following naming rules and flags.

    :param ea: linear address (int)
    :param name: new name (str), None or "" to delete the name
    :param flags: integer flags with combined SN_* bits
    :returns: 1 if successful, 0 if it fails or does nothing
    """
    # Usage example:
    # set_name(0x401000, "main_function", SN_PUBLIC | SN_NOCHECK | SN_FORCE)
    # set_name(0x401010, "local_var", SN_LOCAL | SN_NOCHECK)
    # set_name(0x401020, "", 0)  # Delete name
    # name == None -> do nothing

    if name is None:
        return 0

    # Validate address
    addr = None
    try:
        addr = (
            currentProgram.getAddressFactory().getDefaultAddressSpace().getAddress(ea)
        )
    except Exception:
        if not (flags & SN_NOWARN):
            print(f"Warning: invalid address {ea}")
        return 0

    if addr is None:
        return 0

    # Get the symbol table
    symbol_table = currentProgram.getSymbolTable()

    # Validate name if SN_CHECK is active (default)
    if not (flags & SN_NOCHECK):
        # Check for basic valid characters
        import re

        if not re.match(r"^[a-zA-Z_][a-zA-Z0-9_]*$", name) and name != "":
            if not (flags & SN_NOWARN):
                print(f"Warning: invalid name '{name}' contains invalid characters")
            return 0
    else:
        # SN_NOCHECK: replace invalid characters with '_'
        import re

        if name != "":
            # Replace invalid characters
            name = re.sub(r"[^a-zA-Z0-9_]", "_", name)
            # Ensure it starts with a letter or _
            if name and not re.match(r"^[a-zA-Z_]", name):
                name = "_" + name

    # Get existing symbol at the address
    existing_symbols = symbol_table.getSymbols(addr)
    primary_symbol = None

    for sym in existing_symbols:
        if sym.isPrimary():
            primary_symbol = sym
            break

    try:
        # Delete name if it's an empty string
        if name == "":
            if primary_symbol is not None:
                symbol_table.removeSymbolSpecial(primary_symbol)
            return 1

        # Apply SN_NODUMMY: prefix with '_' if it starts with dummy suffix
        if flags & SN_NODUMMY:
            dummy_prefixes = [
                "sub_",
                "loc_",
                "unk_",
                "byte_",
                "word_",
                "dword_",
                "qword_",
            ]
            for prefix in dummy_prefixes:
                if name.startswith(prefix):
                    name = "_" + name
                    break

        # Generate unique name if SN_FORCE is active
        original_name = name
        counter = 0
        if flags & SN_FORCE:
            while symbol_table.getSymbol(name, addr.getAddressSpace()) is not None:
                counter += 1
                name = f"{original_name}_{counter}"

        # Determine namespace (local vs global)
        namespace = None
        if flags & SN_LOCAL:
            # Look for containing function for local namespace
            function_manager = currentProgram.getFunctionManager()
            containing_function = function_manager.getFunctionContaining(addr)
            if containing_function is not None:
                namespace = containing_function
            else:
                namespace = symbol_table.getGlobalNamespace()
        else:
            namespace = symbol_table.getGlobalNamespace()

        # Determine SourceType
        source_type = SourceType.USER_DEFINED
        if flags & SN_AUTO:
            source_type = SourceType.DEFAULT
        elif flags & SN_NON_AUTO:
            source_type = SourceType.USER_DEFINED

        # Create or update symbol
        new_symbol = None

        if primary_symbol is not None:
            # Update existing symbol
            primary_symbol.setName(name, source_type)
            new_symbol = primary_symbol
        else:
            # Create new symbol
            new_symbol = symbol_table.createLabel(addr, name, namespace, source_type)

        if new_symbol is None:
            if not (flags & SN_NOWARN):
                print(f"Warning: could not create/update symbol '{name}' at {addr}")
            return 0

        # Apply visibility and property flags

        # Public/non-public flags
        if flags & SN_PUBLIC:
            new_symbol.setGlobal()
        elif flags & SN_NON_PUBLIC:
            # In Ghidra, local symbols are managed via namespace
            # There's no direct setPrivate() method; it's namespace-based
            pass

        # Weak/non-weak flags
        # Note: Ghidra doesn't have direct support for "weak" symbols like IDA does
        # This would be disassembler-specific
        if flags & SN_WEAK:
            # Placeholder implementation – Ghidra doesn't directly support weak symbols
            pass
        elif flags & SN_NON_WEAK:
            # Placeholder implementation
            pass

        # SN_NOLIST - exclude from name list
        # Ghidra doesn't have a direct equivalent; handled in UI

        # Handle SN_DELTAIL if necessary
        if flags & SN_DELTAIL:
            # Check for tail byte conflicts and resolve them if needed
            # This would be case-specific
            pass

        return 1

    except DuplicateNameException:
        if not (flags & SN_NOWARN):
            print(f"Warning: the name '{name}' already exists.")
        return 0
    except InvalidInputException:
        if not (flags & SN_NOWARN):
            print(f"Warning: invalid name '{name}'.")
        return 0
    except Exception as e:
        if not (flags & SN_NOWARN):
            print(f"Warning: unexpected error: {str(e)}")
        return 0


# Helper functions to use flags more easily
def set_name_checked(ea, name):
    """Set name with character validation"""
    return set_name(ea, name, SN_CHECK)


def set_name_unchecked(ea, name):
    """Set name replacing invalid characters"""
    return set_name(ea, name, SN_NOCHECK)


def set_name_public(ea, name):
    """Set public name"""
    return set_name(ea, name, SN_PUBLIC | SN_NOCHECK)


def set_name_local(ea, name):
    """Set name local to the function"""
    return set_name(ea, name, SN_LOCAL | SN_NOCHECK)


def set_name_auto(ea, name):
    """Set auto-generated name"""
    return set_name(ea, name, SN_AUTO | SN_NOCHECK)


def set_name_force(ea, name):
    """Set name forcing variations if it already exists"""
    return set_name(ea, name, SN_FORCE | SN_NOCHECK)


